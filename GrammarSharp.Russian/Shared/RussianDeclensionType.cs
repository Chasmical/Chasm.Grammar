namespace GrammarSharp.Russian
{
    /// <summary>
    ///   <para>Defines Russian declension types.</para>
    /// </summary>
    public enum RussianDeclensionType
    {
        /// <summary>
        ///   <para>Specifies that the declension type is unknown.</para>
        /// </summary>
        Unknown,
        /// <summary>
        ///   <para>Specifies a noun declension.</para>
        /// </summary>
        Noun,
        /// <summary>
        ///   <para>Specifies an adjective declension.</para>
        /// </summary>
        Adjective,
        /// <summary>
        ///   <para>Specifies a pronoun declension.</para>
        /// </summary>
        Pronoun,
    }
}
